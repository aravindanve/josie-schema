const GET_DAYS_BY_MONTH = [0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];
function date(data) {
  const matches = data.match(/^(\d\d\d\d)-(\d\d)-(\d\d)$/);

  if (!matches) {
    return false;
  }

  const month = +matches[2];
  const day = +matches[3];

  return month >= 1 && month <= 12 && day >= 1 &&
    day <= (month == 2 && isLeapYear(+matches[1])
      ? 29 : GET_DAYS_BY_MONTH[month]);
}

// source: [ajv](https://github.com/epoberezkin/ajv/blob/master/lib/compile/formats.js)
function time(data) {
  const matches = data.match(/^(\d\d):(\d\d):(\d\d)(\.\d+)?(z|[+-]\d\d:\d\d)?$/i);

  if (!matches) {
    return false;
  }

  const hour = matches[1];
  const minute = matches[2];
  const second = matches[3];
  const tz = matches[5];
  return ((hour <= 23 && minute <= 59 && second <= 59) ||
    (hour == 23 && minute == 59 && second == 60)) && tz;
}

function regex(data) {
  try {
    new RegExp(data);
    return !/[^\\]\\Z/.test(data);

  } catch (e) {
    return false;
  }
}
